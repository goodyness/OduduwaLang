# OduduwaLang Standard Library: iro (Math)

import math

def gbongbo(x):
    """Square root: math.sqrt(x)"""
    return math.sqrt(x)

def agbara(x, y):
    """Power: math.pow(x, y)"""
    return math.pow(x, y)

def poju(*args):
    """Maximum: max(*args)"""
    return max(*args)

def kereju(*args):
    """Minimum: min(*args)"""
    return min(*args)
